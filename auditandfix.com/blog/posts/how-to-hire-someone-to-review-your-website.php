<h2>What should a website review cost?</h2>

<p>Here's a rough guide based on what's in the market right now:</p>

<ul>
    <li><strong>Free automated scans</strong> (SEOptimer, Lighthouse, etc.) — $0, but they check technical metrics, not conversion. Useful as a starting point, not a replacement for human analysis.</li>
    <li><strong>Freelance review</strong> — $50–$200. Quality depends entirely on the individual.</li>
    <li><strong>Specialist one-time audit</strong> — $150–$500. Structured report with prioritised, actionable fixes.</li>
    <li><strong>Agency audit</strong> — $500–$5,000+. Comprehensive, often includes a strategy session. Usually the first step in a larger engagement.</li>
</ul>

<p>For most small businesses, the sweet spot is somewhere in the $150–$500 range. That's enough to get a thorough, expert review without paying for things you don't need.</p>

<h2>Red flags to watch for</h2>

<p>Not all website reviews are created equal. Avoid anyone who:</p>

<ul>
    <li><strong>Only talks about SEO</strong> — Backlinks and meta descriptions won't help if your headline is confusing and your contact form is broken on mobile.</li>
    <li><strong>Delivers a generic template</strong> — If the report could apply to any website, it's worthless. You need specific observations about <em>your</em> site.</li>
    <li><strong>Won't show samples</strong> — A good reviewer should be happy to show you what their output looks like.</li>
    <li><strong>Promises rankings or traffic</strong> — A website review tells you what's wrong and how to fix it. It doesn't guarantee results. Anyone who promises specific outcomes is selling you something else.</li>
    <li><strong>Requires a monthly subscription</strong> — A review is a point-in-time assessment. You shouldn't need to pay monthly for it.</li>
</ul>

<h2>What to do with the review once you have it</h2>

<p>The biggest mistake people make after getting a website review is trying to fix everything at once. Don't. Look at the report, find the top three recommendations that seem most impactful, and do those first. Then measure whether enquiries improve. Then do the next three.</p>

<p>Most conversion improvements come from a handful of changes — a better headline, a clearer CTA, a visible phone number on mobile. You don't need to rebuild your site. You need to fix the three or four things that are actively costing you customers.</p>

