<h2>4. Your page takes too long to load</h2>

<p>If your site takes more than three seconds to load on a mobile phone, you're losing roughly half your visitors before they even see your content. Research consistently shows that roughly half of mobile users abandon a page that takes longer than three seconds to load.</p>

<p>The usual culprits: oversized images that haven't been compressed, too many plugins, cheap hosting, and videos that autoplay on the homepage.</p>

<p><strong>The fix:</strong> Run your URL through Google PageSpeed Insights. If your mobile score is below 50, start with image compression — it's the single biggest win for most small business sites.</p>

<h2>5. You're not speaking to the right person</h2>

<p>A lot of small business websites try to appeal to everyone. The copy is generic, the services list is a mile long, and there's no sense of "this is for <em>you</em>."</p>

<p>The best-converting sites we've analysed speak directly to a specific customer with a specific problem. "Blocked drain at 10pm? We'll be there within the hour" converts better than "We offer a comprehensive range of plumbing services for residential and commercial clients."</p>

<p><strong>The fix:</strong> Pick your most profitable customer type. Write your homepage copy as if you're talking directly to them. Mention their problem before you mention your solution.</p>

<h2>6. Your mobile experience is an afterthought</h2>

<p>More than 60% of small business website traffic comes from mobile phones. Yet most sites are designed desktop-first and then squeezed onto a small screen. Buttons too small to tap, text too small to read, forms that require pinch-zooming — all of these kill mobile conversions.</p>

<p><strong>The fix:</strong> Open your site on your phone right now. Try to complete the main action (call, enquire, book). If it's frustrating, your customers feel the same way.</p>

<h2>7. You have no social proof near the point of decision</h2>

<p>Most sites put testimonials on a dedicated "Testimonials" page that nobody visits. The place social proof matters most is right next to your call to action — at the exact moment someone is deciding whether to contact you.</p>

<p>A review snippet ("Fastest response I've ever had from a tradie — 10/10" — Sarah, Bondi) placed next to your "Get a Free Quote" button can lift conversion rates by 15–30%.</p>

<p><strong>The fix:</strong> Put your best one-line testimonial directly above or below your main CTA on every key page.</p>

