<p>Australian sites lead, but not by a dramatic margin. A few points of context here: our scoring methodology was originally calibrated against Australian businesses, so there may be a slight home-ground advantage baked in. It's also possible that Australian SMBs invest more in their web presence on average – the market is smaller and more competitive for local search, which might push quality up.</p>

<p>The more interesting takeaway is how narrow the range is. Every country's average falls between 64 and 68. The problems are universal. It doesn't matter whether you're a plumber in Perth or a plumber in Portland – the same conversion mistakes keep showing up.</p>

<h2>What Separates a 75 from a 55</h2>

<p>This is the part people find surprising. The difference between a mediocre site and a good one isn't usually a complete redesign. It's typically two or three specific things.</p>

<p>When we look at sites that score 55 and compare them to sites that score 75, the same gaps come up repeatedly:</p>

<h3>Missing or weak calls to action</h3>

<p>The single biggest conversion killer in our dataset. A 55-scoring site might have "Contact Us" in the nav bar and a phone number somewhere in the footer. A 75-scoring site has a prominent, specific CTA above the fold – "Get a Free Quote" or "Book Your Inspection" – with a button that looks like a button and copy that tells the visitor exactly what happens next.</p>

<h3>No trust signals above the fold</h3>

<p>A 55-scoring site mentions reviews on a dedicated testimonials page that nobody visits. A 75-scoring site shows a Google rating, a review count, and two short customer quotes right there on the homepage. Trust displayed where people actually see it, not hidden three clicks deep.</p>

<h3>Generic value propositions</h3>

<p>A 55-scoring site says "Quality [trade] services in [city]." A 75-scoring site says "Same-day emergency plumbing – fixed price, no call-out fee, licensed and insured." One tells the visitor something useful. The other says nothing at all.</p>

<p>None of these require a new website. They don't even require a web developer in most cases. They require someone to look at the page through the eyes of a stranger who has three other tabs open and is deciding who to call.</p>

<h2>The Factors That Tank Scores Most</h2>

<p>Across all 35,529 sites, the conversion factors that score lowest on average are:</p>

<ol>
    <li><strong>Urgency</strong> – Almost nobody gives visitors a reason to act now. This is consistently the weakest factor in our dataset.</li>
    <li><strong>USP</strong> – Most businesses can't articulate what makes them different. Their website reflects that.</li>
    <li><strong>Offer clarity</strong> – Visitors can tell you're a plumber but not what specific services you offer, what the process looks like, or what they'll get when they call.</li>
</ol>

<p>The factors that score highest are imagery/design (most modern templates look decent) and industry appropriateness (most sites at least get the basics of their sector right). In other words, sites tend to look okay on the surface – the problems are in the persuasion layer underneath.</p>

<h2>What This Means If You Run a Local Business</h2>

<p>Statistically, your website probably scores somewhere in the low-to-mid 60s. It probably looks fine. You probably haven't thought about it much since it was built.</p>

<p>And it's probably costing you one or two enquiries a week that you'll never know about.</p>

<p>Those visitors don't send you a message saying "I visited your site but wasn't convinced." They just go back to Google and call someone else. Over a year, at even modest job values, that's tens of thousands of dollars in work that went to a competitor – not because they're better at what they do, but because their website did a slightly better job of converting the same visitor.</p>

<p>The good news is that the gap between a 55 and a 75 is almost always fixable in an afternoon. It's not a design problem. It's a messaging problem. Stronger headline, specific CTA, visible trust signals, clear offer. Those four changes alone account for most of the score difference we see in our data.</p>

