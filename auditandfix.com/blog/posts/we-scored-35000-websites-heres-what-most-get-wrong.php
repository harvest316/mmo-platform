<p>Over the past year, we've built a scanner that grades local business websites on how well they convert visitors into enquiries. Not SEO. Not page speed. Conversion – the thing that actually puts money in your account.</p>

<p>As of this week, we've scored 35,529 sites. Plumbers, electricians, cleaners, pest control, dentists, landscapers, accountants – real small businesses across Australia, the UK, the US, Canada, Germany and France. Every site scored against the same 10 conversion factors.</p>

<p>Nobody else has a dataset like this. SEO tools will tell you about backlinks and meta tags. We wanted to know something different: when a potential customer lands on your website, does the page actually convince them to pick up the phone?</p>

<p>Here's what 35,000 sites taught us.</p>

<h2>The Average Local Business Website Scores a D+</h2>

<p>The mean score across all 35,529 sites is 64.1 out of 100. The median is slightly higher at 67.7. In letter grades, that's a D+ – not failing, but a long way from good.</p>

<p>Here's how the grades break down:</p>

<ul>
    <li><strong>F (below 60):</strong> 22.3% of all sites</li>
    <li><strong>D (60–64):</strong> 12.6%</li>
    <li><strong>D+ (65–69):</strong> 25% – the single most common grade</li>
    <li><strong>C (70–72):</strong> 20.3%</li>
    <li><strong>B- or above (73+):</strong> 10.3%</li>
</ul>

<p>Read that last line again. Fewer than one in ten local business websites score above a B-. And only 35 sites out of 35,529 scored 80 or above.</p>

<p>This isn't a sample of terrible websites. These are real businesses with real customers. Many of them are spending money on Google Ads driving traffic to these pages. The sites work. They load. They have a phone number somewhere. They're just not very good at converting the visitor who's already there.</p>

<h2>What We Actually Measure</h2>

<p>Our scanner evaluates 10 conversion factors. These aren't arbitrary – they're the things that research and testing consistently show affect whether a visitor enquires or bounces. In plain terms:</p>

<ol>
    <li><strong>Headline quality</strong> – Does the first thing a visitor reads make them want to keep reading? Or is it your business name in large font and nothing else?</li>
    <li><strong>Value proposition</strong> – Can a visitor tell within five seconds what you do and why they should care? Most sites assume people already know this. They don't.</li>
    <li><strong>Unique selling proposition (USP)</strong> – What makes you different from the three other businesses they have open in tabs right now? "Quality service" and "family owned" don't count.</li>
    <li><strong>Call to action</strong> – Is there a clear, visible next step? And is it something better than a tiny "Contact Us" link buried in the navigation?</li>
    <li><strong>Trust signals</strong> – Reviews, certifications, guarantees, real photos of your team. Anything that makes a stranger think "these people are legitimate."</li>
    <li><strong>Urgency</strong> – Is there any reason to act now rather than bookmark the tab and forget about it? This doesn't mean fake countdown timers. It means giving people a reason to call today.</li>
    <li><strong>Hook and visual engagement</strong> – Does the page grab attention above the fold? Or does it look like every other template site in your industry?</li>
    <li><strong>Imagery and design</strong> – Real job photos versus stock images. Clean layout versus cluttered. Professional versus amateur.</li>
    <li><strong>Offer clarity</strong> – Can a visitor understand exactly what they'll get if they contact you? Or is it vague waffle about "solutions" and "tailored approaches"?</li>
    <li><strong>Industry appropriateness</strong> – A pest control site needs different things than an accounting firm. We score whether the site matches what visitors in that specific industry expect to see.</li>
</ol>

<p>Every factor is scored independently, then weighted into the overall grade. A site can be strong on design but weak on trust signals. Strong on CTAs but missing a value proposition entirely. The combination is what determines whether someone enquires or clicks back to Google.</p>

<h2>Where the Scores Actually Cluster</h2>

<p>Here's the raw distribution by score range:</p>

<ul>
    <li><strong>0–39:</strong> 354 sites (1%)</li>
    <li><strong>40–49:</strong> 2,485 sites (7%)</li>
    <li><strong>50–59:</strong> 7,425 sites (20.9%)</li>
    <li><strong>60–69:</strong> 17,793 sites (50.1%)</li>
    <li><strong>70–79:</strong> 7,437 sites (20.9%)</li>
    <li><strong>80+:</strong> 35 sites (0.1%)</li>
</ul>

<p>Half of all sites sit in the 60–69 range. We call this the "deadly middle."</p>

<h2>The Deadly Middle</h2>

<p>A site that scores 30 knows it has a problem. The owner can see it. Visitors can see it. It's obviously not working. These are the easiest sites to improve because the gap is visible.</p>

<p>A site that scores 63 is a different story. It looks fine. It's got a decent logo, reasonable layout, a phone number in the header. The owner built it on Squarespace or had a web designer put it together a few years ago. It doesn't look broken.</p>

<p>But it's quietly losing 20–40% of the enquiries it should be getting.</p>

<p>The problem with a D+ site is that nothing screams "fix me." The business owner looks at it and thinks it's fine. Their mates look at it and say it looks good. Meanwhile, a competitor down the road with a 75 is converting the same traffic at nearly double the rate – and the D+ business never knows why their phone rings less.</p>

<p>This is the group we find hardest to reach. Not because they're dismissive, but because they genuinely don't know the problem exists. Their site works. It just doesn't work well.</p>

