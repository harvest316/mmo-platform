<h2>The Step-by-Step: How It Actually Gets Made</h2>

<p>If you're doing this manually, here's the process:</p>

<ol>
    <li><strong>Pull your best reviews</strong> — Look for reviews that are 3–5 sentences, specific, and mention a concrete outcome ("fixed same day", "no more ants", "spotless before the inspection").</li>
    <li><strong>Choose a voiceover</strong> — Use a tool like ElevenLabs or Murf to generate a natural-sounding voiceover reading the review text. Pick a voice that matches your brand tone.</li>
    <li><strong>Source B-roll footage</strong> — Sites like Pexels and Storyblocks have free and low-cost commercial-use footage. Search for your trade and download 3–5 clips.</li>
    <li><strong>Edit in CapCut or similar</strong> — Sequence the footage, add the voiceover, auto-generate captions, add your business name and a CTA frame. Export at 1080×1920.</li>
    <li><strong>Post and schedule</strong> — Upload to Instagram, TikTok, YouTube Shorts and Facebook separately (each platform has its own upload quirks).</li>
</ol>

<p>End-to-end, that's 45–90 minutes per video if you're efficient. One video per week means roughly an hour a week spent on this, indefinitely.</p>

<p>The automated version — which is what Audit&Fix does — reduces that to zero. You connect your Google Business Profile once. Each week, a new review is turned into a video and you receive it ready to post (or it posts automatically, depending on your setup). The selection, voiceover, editing and formatting all happen without your involvement.</p>

<h2>Which Reviews Make the Best Videos?</h2>

<p>Not every review translates equally well. The reviews that produce the strongest videos share a few characteristics:</p>

<ul>
    <li><strong>Specific outcomes</strong> — "They found the nest in the roof cavity and cleared it completely" is better than "Great service". Specifics are credible.</li>
    <li><strong>Emotional language</strong> — "I was panicking about the inspection" or "relieved it was finally sorted" adds humanity that converts.</li>
    <li><strong>Mention of the situation</strong> — Reviews that describe the problem (not just the solution) give the viewer something to identify with.</li>
    <li><strong>Length</strong> — Reviews of 40–100 words read naturally at video pace. Very short reviews (under 20 words) can feel thin; very long ones need editing down.</li>
</ul>

<p>Star rating alone isn't the filter. A four-star review with three detailed sentences will outperform a five-star review that says "Highly recommend!" every time.</p>

